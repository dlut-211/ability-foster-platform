package edu.dlut.ssdut.abilityfosterplatform.model;

public class FulltextIndexColumns {
    private Integer objectId;

    private Integer columnId;

    private Integer typeColumnId;

    private Integer languageId;

    private Integer statisticalSemantics;

    public Integer getObjectId() {
        return objectId;
    }

    public void setObjectId(Integer objectId) {
        this.objectId = objectId;
    }

    public Integer getColumnId() {
        return columnId;
    }

    public void setColumnId(Integer columnId) {
        this.columnId = columnId;
    }

    public Integer getTypeColumnId() {
        return typeColumnId;
    }

    public void setTypeColumnId(Integer typeColumnId) {
        this.typeColumnId = typeColumnId;
    }

    public Integer getLanguageId() {
        return languageId;
    }

    public void setLanguageId(Integer languageId) {
        this.languageId = languageId;
    }

    public Integer getStatisticalSemantics() {
        return statisticalSemantics;
    }

    public void setStatisticalSemantics(Integer statisticalSemantics) {
        this.statisticalSemantics = statisticalSemantics;
    }
}