package edu.dlut.ssdut.abilityfosterplatform.model;

public class DestinationDataSpaces {
    private Integer partitionSchemeId;

    private Integer destinationId;

    private Integer dataSpaceId;

    public Integer getPartitionSchemeId() {
        return partitionSchemeId;
    }

    public void setPartitionSchemeId(Integer partitionSchemeId) {
        this.partitionSchemeId = partitionSchemeId;
    }

    public Integer getDestinationId() {
        return destinationId;
    }

    public void setDestinationId(Integer destinationId) {
        this.destinationId = destinationId;
    }

    public Integer getDataSpaceId() {
        return dataSpaceId;
    }

    public void setDataSpaceId(Integer dataSpaceId) {
        this.dataSpaceId = dataSpaceId;
    }
}